# The signals extracted with load_all_data and load_single_subject_data are associated to a specific recording sessions
# and contain 3 trials and 300 (more or less) 1 second-long active segments, with relaxation time and no-active segments in the middle.
# So, we need a function that segments the signal, extracting only the active 1 seconds segments

def segment_data(eeg, egui):
    #Input:
    #  eeg: signal associated to a single file/session of recording
    #  egui: associated egui
    # Output:
    #  y : label vector (1 label associated to each segment, with the corresponding egui state)
    #  segmented_data : 1 seconds segments of EEG signal
    segmented_data = []
    y = []
    segment = []
    for idx_t in range(eeg.shape[0] - 1):
        segment.append(idx_t)
        label = egui[idx_t][0]
        if label != egui[idx_t+1][0]:
            if idx_t != 0:
                segmented_data.append(eeg[segment][:]) #I also eliminate the 22th channel
                y.append(label)
            segment = []
    return y, segmented_data

# For data characterization purposes, we want to extract only the segments associated to a certaing EGUI/task
# This function does that.

def extract_task_trials(task, segmented_data, y):
    # Input:
    #   - task : str, the EGUI/task we are interested into
    #   - segmented_data: EEG segments extracted from a recording session
    #   - y : label vector corresponding to segmented_data
    # Output:
    #   - an array containing 1 seconds segments associated to the specified EGUI/task
    #[before the duration was close to 1 sec but could vary, we needed to stadardize the window in order to convert into np.array]
    label = tasks_labels[task]
    tasks = []
    for i, el in enumerate(y):
        if el == label:
            tasks.append(segmented_data[i][0:200])
    return np.array(tasks).T

# In order to remove from the segments, the "non-active" segments, so the segments associated to the EGUI:
#  - 0: nothing is happening
#  - 91: session break (intervals between different sessions)
#  - 92: experiment end
#  - 99: initial relaxation

def removing_notuseful_egui(eeg, egui):
    # Input:
    #   - eeg, egui: segments and associated labels from a specific session
    # Ouput:
    #   - eeg, egui: segments and associated labels, discarding not useful EGUI states
    new_eeg = []
    new_egui = []
    for segment in range(len(eeg)):
        if egui[segment] in [91,92,99,0]:
            continue
        else:
            new_eeg.append(eeg[segment][0:200])
            new_egui.append(egui[segment])

    return np.array(new_eeg), np.array(new_egui)

