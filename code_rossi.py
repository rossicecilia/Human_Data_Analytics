# Function to define the windows to compute the PSD.

def return_windows(initial_f, end_f, width, overlap=50):
    # Input:
    #    -  initial_f : start of the window
    #    -  end_f : window end
    #    -  width : window width
    #    -  overlap : overlap of windows [in percentage] for the shift
    # Output :
    #    - window : a list of windows

    #count = 0
    windows = []
    while True:
        #count += 1
        #print("Windows #", count)
        windows.append([int(initial_f), int(initial_f+width)])
        #print(initial_f, initial_f+width)
        initial_f += overlap
        if initial_f + width > end_f:
            break
    return windows

"""Since I was not satisfied by the scipy.signal.stft function, in terms of resolution of the returned images, I created my own custom function for the computation of the STFT (Short-Time Fourier Transform). This function uses the Welch algorith to compute the Power Spectral Density using "Hann" moving windows of wlength dimention, with a shift equal to wshift, computing the PSD on a number of frequency samples equal to nfft.
With respect to the scipy function, this function enables us to mantain high resolution both along the frequency and time axis.
"""

def my_STFT(signal, wlength, wshift, samplerate, nfft=2000, subset_freq=None):
    ### Function to compute the Short-Time Fourier Transform, using the Welch algorithm to compute the PSD
    # Input:
    #   - data: samples (1D matrix)
    #   - wlength : outer window dimention (in sec)
    #   - wshift : shift of the outer window (in sec)
    #   - pshift : shift of the internal window (in sec)
    #   - samplerate : data sample rate (in Hz)
    #   - length of the moving average (default 1 sec)
    # Output:
    #  - signal_STFT: 2D np.array of dim [frequencies x time_samples], containing the STFT

    signal_STFT = []
    outer_windows = return_windows(0, len(signal), width=wlength*samplerate, overlap=wshift*samplerate)
    for outer_win in outer_windows:
        win = signal[outer_win[0]:outer_win[1]]
        f, sxx = scipy.signal.welch(win, fs=200, nperseg=len(win), nfft = nfft, window="hann", scaling="density")
        if np.any(subset_freq) != None:
            freq_range = (f >= subset_freq[0]) & (f <= subset_freq[1])
            f = f[freq_range]
            sxx = sxx[freq_range]
        signal_STFT.append(sxx)

    return np.array(signal_STFT).T

"""More than the simple STFT, one of the most indicative features in terms of spectral content for the discimination of MI activation, is the ERD/ERS (Event Related Desynchronization- Synchronization), measured as the difference between the Power during an MI task and during relaxation. ERD/ERS is very task and spatially specific."""

def my_ERD_ERS(stft_action, stft_baseline, perc=False):
  # Input:
  #   - stft_action: stft for an action segment  [channels x freq x time]
  #   - stft_baseline: stft for a relaxation segment
  #   - perc: bool, if I simply want the difference between the two or the perc format
  erd_ers = []
  for ch in range(stft_action.shape[0]):
    if perc:
      erd_ers.append((((stft_action[ch] - stft_baseline[ch])/stft_baseline[ch])*100))
    else:
      erd_ers.append(stft_action[ch] - stft_baseline[ch])
  return np.array(erd_ers)

