# In order to return the index associated to a specific electrode name
def finding_electrode_position(el_name, A_removed=False, channels_names=CHANNELS_NAMES):
  # el_name : str, associated to the electrode name
  # A_removed: bool, if we have already removed the A1 and A2 channels [Ear References]
    pos = 0
    for key in CHANNELS_NAMES.keys():
        if CHANNELS_NAMES[key] == el_name:
            pos = key
            if A_removed and key > 11:
                pos -= 2
    return pos

# Among the 21th electrodes/channels (the 22th channel has already been discarded since it was not associated to an EEG recording)
# the 11th and 12th channels are A1 [left ear] and A2 [right ear] associated to the Ear References. Therefore, they do not contain any useful informations
# regarding brain activity and have to be removed, for computational speed reason.
# Also, it could be useful to subtract the reference signal measured by A1 and A2 from the EEG signals recorded from all the other electrodes

def removing_Achannels(signal, reference=False, map_reposition_channels=MAP_REPOSITION_CHANNELS):
    # signal : EEG signal, nchannels must be the first dimension
    # reference : bool, if True we also subtract the A1 and A2 references to all the other channels
    new_signal = []
    for ch in range(signal.shape[0]):
        if CHANNELS_NAMES[ch] in ['A1', 'A2', 'X3']:
            continue
        else:
            new_pos = map_reposition_channels[ch]
            if reference:
                if CHANNELS_NAMES[ch] in ['Cz', 'Fz', 'Pz']:
                    new_signal.append(signal[ch] - ((signal[10] + signal[11])/2))
                elif int(CHANNELS_NAMES[ch][-1]) % 2 == 0:
                    new_signal.append(signal[ch] - signal[11]) #if on the right side we subtract the right ear reference
                else:
                    new_signal.append(signal[ch] - signal[10]) #instead if on the left side we subtract the left ear reference
            else:
                new_signal.append(signal[ch])
    return np.array(new_signal)

# Function for channel selection, from a specific channel subset
# For example: it will be useful for MI classification, to extract only the channels localized in the sensory-motor cortex

def channel_selection(signal, channel_to_select, channels_names=CHANNELS_NAMES, removed_A=True):
    # Input:
    #   - signal : np.array, EEG signal where the first dim must be associated to the channels/electrodes
    #   - channel_to_select: list of strings, with the names of the channels to keep
    #   - removed_A: bool, it gives us info regarding the previous removal of the Ear references
    # Output:
    #   - new_signal : np.array, EEG_signals with a reduction in the first dimension, associated to the channel selection
    new_signal = []
    for ch in range(signal.shape[0]):
      if ch >= 10 and removed_A:
        ch_name = channels_names[ch+2]
      else:
        ch_name = channels_names[ch]
      if ch_name in channel_to_select:
        new_signal.append(signal[ch])

    return np.array(new_signal)

"""*Spatial Filtering*"""

# SPATIAL FILTERING is a technique used in order to enhance the spatial informations of the EEG signal.
# It can be applied by using different methods. This function works with:
#   - EarRef : the simple subtraction of the Ear References to all the other channels
#   - 'CAR': Common Average Reference Method, consists into subtracting to all the channels, the mean of all the channels
#   - 'Laplacian' : consists into subtracting to each channel the mean of the signals recording by the neightbours channels

def spatial_filtering(signal, method='EarRef', A_removed=False, channels_names=CHANNELS_NAMES):

    filtered_signal = [] #it's channels x samples

    if method == 'CAR':
        #at each channel we are gonna subtract the mean of all the others
        mean_signal = np.mean(signal, axis=0) #is the mean along all the
        for ch in range(signal.shape[0]):
            filtered_signal.append(signal[ch] - mean_signal)

    elif method == 'EarRef':
        filtered_signal = removing_Achannels(signal, reference=True) #this doesn't work if we have already removed the ear reference

    elif method == 'Laplacian':
        for ch in range(signal.shape[0]):
          if A_removed and ch >= 10:
            n_el = CHANNELS_NAMES[ch+2]
            if n_el == 'X3':
              continue
          else:
            n_el = CHANNELS_NAMES[ch]
            if n_el in ['A1','A2']:
              continue
          mean_signal = np.mean([signal[finding_electrode_position(x, A_removed=A_removed)] for x in NEIGHBOURS[n_el]], axis=0)
          filtered_signal.append(signal[ch] - mean_signal)

    return np.array(filtered_signal)

"""*Temporal Filtering*"""

# The brain activity is physiologically associated to the frequencies between 0.5 and 30/35 Hz.
# Frequency content outside this range can be associated to artifacts (both related to technologies and power-line interferences, and other biological signals like ECG or EOG)
# and therefore needs to be removed. This process is called temporal filtering.
# This function implements it by using:
#  - firstly a Notch Filter that removes the spectral content at f_notch [50] Hz (so possibly associated to power-line interferences)
#  - and then a Band-pass filter (using a Butterwoth filter of order N) between f_ins[0.5] and f_out [40] Hz.

def temporal_filtering(signal, f_notch, f_in, f_out, Q, N, fs=200):
    # Input:
    #  - signal  : EEG signal (could be raw or after the application of spatial filtering)
    #  - f_notch : frequency at which applying the Notch Filter (in Hz)
    #  - f_ins, f_out : the frequencies for the band-pass filter (in Hz)
    #  - Q : quality factor of the notch filter (int)
    #  - N : Butterworth filter order (int)
    #  - fs = sample_frequency of the signal (in Hz)
    # Output:
    #  - filtered_signal
    b_notch_filt, a_notch_filt = scipy.signal.iirnotch(f_notch, Q, fs)
    sos_bp = scipy.signal.butter(N=N, Wn = np.array([f_in,f_out]), btype='bandpass', output='sos', fs=fs)
    filtered_signal = []
    for ch in range(signal.shape[0]):
        filtered_ch = scipy.signal.filtfilt(b_notch_filt, a_notch_filt, signal[ch])
        filtered_ch = scipy.signal.sosfilt(sos_bp, filtered_ch)
        filtered_signal.append(filtered_ch)
    return np.array(filtered_signal)

