#Creating noisy components from high-frequency content of the various signals,
# applying a high-pass filter and extracting high frequency content of the non processed signals
# (so a component not likely to be associated with neural activity)
def return_noisy_components(segments):
  noisy_components = []
  signals_without_noise = []
  for segment in segments:
    sos_hp_1 = scipy.signal.butter(N=8, Wn=90, btype='highpass', output='sos', fs=FS)
    filtered_signal1 = []
    signal_wt_noise = []
    for ch in range(segment.shape[0]):
      filtered_ch_1 = scipy.signal.sosfilt(sos_hp_1, segment[ch])
      filtered_signal1.append(filtered_ch_1)
      signal_wt_noise.append(segment[ch] - filtered_ch_1)

    filtered_signal1 = np.array(filtered_signal1)
    signal_wt_noise = np.array(signal_wt_noise)

    noisy_components.append(filtered_signal1)
    signals_without_noise.append(signal_wt_noise)

  noisy_components = sklearn.utils.shuffle(noisy_components)
  return noisy_components, signals_without_noise #I want it to be a list, so I can use the pop method

#After randomly shuffling the noisy components, we create 3 new augmented samples for each segment,
# by summing it with a random picked noisy component.
def create_augmented_dataset(segments, egui, noisy_components):
  new_egui = []
  new_segments = []
  for i,segment in enumerate(segments):
    idx1 = np.random.randint(0, len(noisy_components)-1)
    idx2 = np.random.randint(0, len(noisy_components)-1)
    idx3 = np.random.randint(0, len(noisy_components)-1)

    new_segment1 = segment + noisy_components[idx1]
    new_segment2 = segment + noisy_components[idx2]
    new_segment3 = segment + noisy_components[idx3]

    new_segments.append(new_segment1)
    new_segments.append(new_segment2)
    new_segments.append(new_segment3)
    for _ in range(3):
      new_egui.append(egui[i])

  return np.array(new_segments), np.array(new_egui)

