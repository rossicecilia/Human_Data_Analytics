#Since the files are saved with a format YYMMDD,
#this is a function to convert the normal datetime YYYY-MM-DD format into a string for data loading

def from_datetime_to_str(d):
    # Input: d = date in datetime YYYY-MM-DD format
    # Output date_str = string with the same date in 'YYMMDD' format
    year_str = str(d.year)[2:]
    month_str = str(d.month) if len(str(d.month)) >1 else '0'+str(d.month)
    day_str = str(d.day) if len(str(d.day)) >1 else '0'+str(d.day)
    date_str = year_str+month_str+day_str
    return date_str

