  #  - new_shape : reshaping of the STFT output ( original dimension: nchannels x frequency x time_samples)
  # Output:
  #  - signal : 3D STFT features of shape [nchannels x frequencies x temporal_samples] OR [temporal_samples x frequencies x nchannels] OR ... (depending on new_shape)

  data = processing(data, spatial_filt=spatial_filt, temporal_filt=temporal_filt, channel_sel = channel_sel, normalize=False)

