def processing(data, spatial_filt=True, temporal_filt=True, channel_sel=False, normalize=True):
  # Input:
  #   - data is a single EEG segment [time_samples x nchannels]
  #   - spatial_filt: bool (if applying or not the spatial filtering)
  #   - temporal_filt: bool (if applying or not the temporal filtering)
  #   - channel_sel: bool (if applying or not the channel_selection)
  #   - normalize: bool (if applying or not data normalization)
  # Output:
  #   - data : np.array with processed EEG segment (we also change the data type in np.float32 making it compatible with the TENSOR FLOW Datasets and Models)

  #Firstly we transpose the segment --> so we obtain nchannels x time_samples
  data = data.T

  #Then we remove Achannels
  data = removing_Achannels(data, False)

  #Then we apply a spatial filtering
  if spatial_filt:
    data = spatial_filtering(data, method=SPATIAL_METHOD, A_removed=True, channels_names=CHANNELS_NAMES)

  #Finally, we apply temporal filtering
  if temporal_filt:
    data = temporal_filtering(data, f_notch=F_NOTCH, f_in = F_IN, f_out=F_OUT, Q=Q, N=N_BUTTER, fs=FS)

  #Eventually, Channel selection
  if channel_sel:
    data = channel_selection(data, CHANNELS_MI, CHANNELS_NAMES)

  #After the processing we also normalize the data
  if normalize:
    data = normalize_data(data)

  return data.astype(np.float32)

def creating_raw_dataset(eeg, egui, batch_size, shuffle=False, cache_file=None, spatial_filt=True, temporal_filt=True, channel_sel=False, sample_rate=FS, normalize=True):
    #Input data will be nchannels x time_samples matrix
    #I have the y_HaLT_A, eeg_segmented_HaLT_A

    dataset = tf.data.Dataset.from_tensor_slices((eeg, egui))

    # Applying the processing and stft features extraction (all condensed in the same function)
    py_process = lambda data, label: (tf.numpy_function(processing, [data, spatial_filt, temporal_filt, channel_sel, normalize], tf.float32), label)
    dataset = dataset.map(py_process, num_parallel_calls=os.cpu_count())

    # Cache dataset
    if cache_file:
        dataset = dataset.cache(cache_file)

    # Shuffle
    if shuffle:
        dataset = dataset.shuffle(len(egui))

    # Repeat the dataset indefinitely
    dataset = dataset.repeat()

    # Map the crop function
    input_size = (3, 200) if channel_sel else (19,200)
    py_func5 = lambda data, label: (tf.ensure_shape(data, input_size), label)
    dataset = dataset.map(py_func5)

    #I need to add 1 dimention
    dataset = dataset.map(lambda data, label: (tf.expand_dims(data,-1), label))

    # Correct input type
    py_float = lambda data, label: (tf.cast(data, tf.float32), tf.cast(label, tf.float32))
    dataset = dataset.map(py_float)

    # Batch
    dataset = dataset.batch(batch_size=batch_size)

    # Prefetch
    dataset = dataset.prefetch(buffer_size=1)

    return dataset

"""*STFT feature extraction*:
Contains both the processing part and the computation of the STFT features. Also, since the STFT returned by my_STFT has shape frequencies x time_samples, we could want to reshape it:
 - in [channels x time_samplex x frequencies] --> new_shape [0,2,1]
 - in [time_samples x frequencies x channels] --> new_shape [2,1,0].
For this we use the my_reshape custom function (since np.reshape doesn't work properly).
"""

def my_reshape(data, new_shape):
  if new_shape[0] ==  0 and new_shape[1] == 2: #[0,2,1]
    #input of EEGNet_nofirstlayer
    #we start from channels x frequencies x samples (19 x 50 x 181)
    # and we want to get 19 x 181 x 50
    new_data = []
    for ch in range(data.shape[0]):
      new_data.append(data[ch].T)

  elif new_shape[0] == 2 and new_shape[1] == 1: # [2,1,0]:
    #input of EEGNet
    #we start from channels x frequencies x samples (19 x 50 x 181)
    # and we want to get 181 x 50 x 19
    new_data = np.zeros(shape=(data.shape[2], data.shape[1], data.shape[0]))
    for ch in range(data.shape[0]):
      new_data[:, :, ch] = data[ch].T

  return np.array(new_data)

