"""# NEW MODELS

Trying out 2 approeaches that could potentially help improving the EEGNet model:

- Attention layers --> that enables to focus on significant features

- the use of Residual Connections (summing the
output of previous layer with the new output)
 or/and Inception modules (more convolution one close to the other that are then concatenated)

Finally, we could also think of combining the Attention mechanisms with Inception/Residuals layers.
"""

def EEGNet_with_Attention(nb_classes,
                                   Chans=19,
                                   Samples=200,
                                   dropoutRate=0.5,
                                   kernLength=100,
                                   F1=8,
                                   D=2,
                                   F2=16,
                                   norm_rate=0.25,
                                   dropoutType='Dropout'):

    if dropoutType == 'SpatialDropout2D':
        dropoutType = SpatialDropout2D
    elif dropoutType == 'Dropout':
        dropoutType = Dropout
    else:
        raise ValueError('dropoutType must be one of SpatialDropout2D or Dropout, passed as a string.')

    input1 = Input(shape=(Chans, Samples, 1))

    ##################################################################
    # First Convolutional Block
    block1 = Conv2D(F1, (1, kernLength), padding='same',
                    input_shape=(Chans, Samples, 1),
                    use_bias=False)(input1)
    block1 = BatchNormalization()(block1)
    block1 = DepthwiseConv2D((Chans, 1), use_bias=False,
                             depth_multiplier=D,
                             depthwise_constraint=max_norm(1.))(block1)
    block1 = BatchNormalization()(block1)
    block1 = Activation('elu')(block1)

    ##################################################################
    # Temporal Attention Mechanism
    # Apply attention over the time dimension
    temporal_attention = GlobalAveragePooling2D()(block1) #computing the global mean (for all the D*F1 features maps on the time) --> this gives us a sort of "key"/"hidden representation" of the feature map
    temporal_attention = Dense(F1 * D, name="dense1")(temporal_attention)
    temporal_attention = Activation('relu')(temporal_attention)
    temporal_attention = Dense(F1 * D, name="dense2")(temporal_attention)
    temporal_attention = Activation('sigmoid')(temporal_attention)
    temporal_attention = Reshape((1, 1, F1 * D))(temporal_attention) #I'm reshaping in a 2D matrix
    block1_ta = Multiply()([block1, temporal_attention])

    ##################################################################
    # Spatial Attention Mechanism
    # Apply attention over the channel dimension
    spatial_attention = Lambda(lambda x: K.mean(x, axis=-1, keepdims=True))(block1)  # Global average pooling on the time axis
    spatial_attention = Conv2D(F1*D, (1, 1), activation='relu', use_bias=False)(spatial_attention)
    spatial_attention = Activation('sigmoid')(spatial_attention)
    block1_sa = Multiply()([block1, spatial_attention])

    ########### RESIDUAL CONNECTION

    block1 = add([block1, block1_ta, block1_sa])
    block1 = AveragePooling2D((1, 4))(block1)
    block1 = dropoutType(dropoutRate)(block1)

    ##################################################################
    # Second Convolutional Block
    block2 = SeparableConv2D(F2, (1, 16),
                             use_bias=False, padding='same')(block1)
    block2 = BatchNormalization()(block2)
    block2 = Activation('elu')(block2)
    block2 = AveragePooling2D((1, 8))(block2)
    block2 = dropoutType(dropoutRate)(block2)

    ##################################################################
    # Final Classification Layer
    flatten = Flatten(name='flatten')(block2)
    dense = Dense(nb_classes, name='dense',
                  kernel_constraint=max_norm(norm_rate))(flatten)
    softmax = Activation('softmax', name='softmax')(dense)

    return Model(inputs=input1, outputs=softmax)

