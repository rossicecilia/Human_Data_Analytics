def EEGInception(nb_classes=6,
                 Chans=19,
                 Samples=200,
                 dropoutRate=0.5,
                 F1=8,
                 D=4,
                 F2 = 32,
                 norm_rate=0.25,
                 dropoutType = 'Dropout'):

  if dropoutType == 'SpatialDropout2D':
    dropoutType = SpatialDropout2D
  elif dropoutType == 'Dropout':
    dropoutType = Dropout
  else:
    raise ValueError('dropoutType must be one of SpatialDropout2D '
                         'or Dropout, passed as a string.')

  input1 = Input(shape=(Chans, Samples, 1)) #19 x 200 x 1

  ############ INCEPTION LAYER ####################
  #Bottlenecklayer
  block1 = Conv2D(F1, (1,1), padding="same")(input1)
  ### PARALLEL CONVOLUTIONS
  conv1 = Conv2D(F1, (1, 20), padding="same", input_shape = (Chans, Samples, 1), use_bias=False)(block1) #window of 100 ms
  conv2 = Conv2D(F1, (1, 40), padding="same", input_shape = (Chans, Samples, 1), use_bias=False)(block1) #window of 200 ms
  conv3 = Conv2D(F1, (1, 80),  padding="same", input_shape = (Chans, Samples, 1), use_bias=False)(block1) #window of 300 ms
  conv4 = Conv2D(F1, (1, 100),  padding="same", input_shape = (Chans, Samples, 1), use_bias=False)(block1) #window of 500 ms

  ### Pooling Layer
  pool1        = MaxPooling2D(pool_size=(1,20))(input1)
  conv_pool    = Conv2D(F1, (1,1), padding="same")(input1)

  ######## CONCATENATION
  incept1      = concatenate([conv1, conv2, conv3, conv4, conv_pool], axis=1)
  incept1      = BatchNormalization()(incept1)

  ############ DEPTHWISECONV2D ####################

  block2       = DepthwiseConv2D((95, 1), use_bias = False,
                                   depth_multiplier = D,
                                   depthwise_constraint = max_norm(1.))(incept1)
  block2       = BatchNormalization()(block2)
  block2       = Activation('elu')(block2)
  block2       = AveragePooling2D((1,8))(block2)
  block2       = dropoutType(dropoutRate)(block2)

  ############ FINAL CLASSIFICATION LAYER ##########

  block3       = SeparableConv2D(F2, (1, 16), use_bias = False, padding = 'same')(block2) #non lo vede
  block3       = BatchNormalization()(block3)
  block3       = Activation('elu')(block3)
  block3       = AveragePooling2D((1, 8))(block3)
  block3       = dropoutType(dropoutRate)(block3)

  flatten      = Flatten(name = 'flatten')(block3)

  dense        = Dense(nb_classes, name = 'dense',
                         kernel_constraint = max_norm(norm_rate))(flatten)
  softmax      = Activation('softmax', name = 'softmax')(dense)

  return Model(inputs=input1, outputs = softmax)

