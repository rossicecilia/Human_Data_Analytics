# In order to normalize the EEG signals
# We do that using the Z-score normalization

def normalize_data(data):
    # Amplitude estimate
    signal_mean = np.mean(data)
    signal_std = np.std(data)
    return ((data - signal_mean) / signal_std)

# In order to normalize the STFT np.arrays (3D images) between [-1, 1]
def normalize_negative_one(img):
  # img : is the STFT array (nchannels x frequencies x time)
  normalized_img = []
  for ch in range(img.shape[0]):
    normalized_input = (img[ch] - np.amin(img[ch])) / (np.amax(img[ch]) - np.amin(img[ch]))
    normalized_img.append(2*normalized_input - 1)
  return np.array(normalized_img)

